from array2D import slice_me


print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 0, 2))
print()
print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 1, -2))
print()
print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], -1, -2))
print()
print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], -5, 2))
print()
print(slice_me([[1.80], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 1, -2))
print()
print(slice_me([[1.80, 78.4, 42.42], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 1, -2))
print()
print(slice_me(["42", [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 1, -2))
print()
print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], [42], -2))
print()
print(slice_me([[1.80, 78.4], [2.15, 102.7], [2.10, 98.5], [1.88, 75.2]], 1, "42"))
